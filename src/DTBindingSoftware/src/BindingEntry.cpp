// BUG-0136:
// CBindingEntry::Clear( ) --> m_nID member wordt niet geinitialiseerd.
void CBindingEntry::Clear(void)
{
	CString szNewName((LPCSTR) IDS_DEFAULT_BINDING_NAME);

	m_ulApplicationFlags = 0;

	m_BindingLinks.RemoveAll();
	m_BindingOperators.RemoveAll();
	m_BindingUnits.RemoveAll();

	m_BindingEvaluationString.Clear();
	
	SetBindingReturnFlags(BINDINGFLAGS_STATUS_NOCHANGE);	
	SetName(szNewName);
	SetBindingNr(0);	
	SetSend(FALSE);

	ASSERT(m_BindingLinks.Count() == 0);
	ASSERT(m_BindingOperators.Count() == 0);
	ASSERT(m_BindingUnits.Count() == 0);
}

BOOL CBindingEntry::Verify()
{
	CString szTemp(m_BindingEvaluationString.Get());

	if (szTemp.IsEmpty() == FALSE)
	{
		if (m_BindingEvaluationString.Parse() == TRUE)
		{
			if (m_BindingUnits.Count() != (m_BindingEvaluationString.GetNrOfInputs() +
										   m_BindingEvaluationString.GetNrOfOutputs()))
			{	
				ASSERT(0);			
				return FALSE;
			}
			return TRUE;
		}
		ASSERT(0);
	}
	ASSERT(0);
	return FALSE;
}

// Utilities.
BOOL CBindingEntry::getFlagAutoGenerated(const CBindingEntry& r) {
	return (APPFLAG_AUTO_GENERATED == (r.GetApplicationFlags() & APPFLAG_AUTO_GENERATED));
}
