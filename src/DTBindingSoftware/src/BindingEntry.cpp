CBindingEntry::CBindingEntry()
{
	Clear();
}
CBindingEntry::CBindingEntry(const CBindingEntry& BindingEntry)
{
	Copy(&BindingEntry);
}
CBindingEntry& CBindingEntry::operator=(const CBindingEntry& BindingEntry)
{
	if (this == &BindingEntry)
		return (*this);
	
	Copy(&BindingEntry);
	return(*this);
}

CBindingEntry::~CBindingEntry()
{
}

void CBindingEntry::Copy(const CBindingEntry* const pSource)
{
	if (pSource == 0) 
	{
		throw new CExceptionDatabase("Pointer to null");
	}
				
	Clear();		

	m_BindingLinks = pSource->m_BindingLinks;
	m_BindingOperators = pSource->m_BindingOperators;
	m_BindingUnits = pSource->m_BindingUnits;
	m_BindingEvaluationString = pSource->m_BindingEvaluationString;
		
	m_nID = pSource->m_nID;
	m_szName = pSource->m_szName;
	m_bBindingReturnFlags = pSource->m_bBindingReturnFlags;
	m_usBindingNr = pSource->m_usBindingNr;
	m_fState = pSource->m_fState;	
	m_ulApplicationFlags = pSource->m_ulApplicationFlags;		
}

